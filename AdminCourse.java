     public void AdminCourse_Load(){
        int c;
        try {
            pst=con.prepareStatement("select * from courses");
            rs=pst.executeQuery();
             
            ResultSetMetaData rsd= rs.getMetaData();
            c=rsd.getColumnCount();
            d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2=new Vector();
                
                for(int i=1;i<=c;i++)
                {
                    
                    v2.add(rs.getString("programme"));
                    v2.add(rs.getString("code"));
                    v2.add(rs.getString("course1"));
                    v2.add(rs.getString("course2"));
                    v2.add(rs.getString("course3")); 
                    v2.add(rs.getString("course4"));
                    v2.add(rs.getString("course5"));
                } 
                
                d.addRow(v2);
                
            }
                   
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    
    
    
    
    
