    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try {
             d=(DefaultTableModel)jTable1.getModel();
     int  selectIndex=jTable1.getSelectedRow();
     
      txtpro.setText(d.getValueAt(selectIndex, 0).toString());
            
            String programme=txtpro.getText();
            String code=txtcode.getText();
           String course1=txtcs1.getSelectedItem().toString();
            String course2=txtcs2.getSelectedItem().toString();
            String course3=txtcs3.getSelectedItem().toString();
            String course4=txtcs4.getSelectedItem().toString();
            String course5 =txtcs5.getSelectedItem().toString();
          
            pst=con.prepareStatement("update courses set code=?,course1=?, course2=?,course3=?,course4=?,course5=? where programme=?");
            
            //pst.setString(1, name);
            pst.setString(1, code);
            pst.setString(2, course1);
            pst.setString(3, course2);
            pst.setString(4,course3);
            pst.setString(5,course4);
            pst.setString(6,course5);
            pst.setString(7, programme);
             pst.executeUpdate();
             JOptionPane.showMessageDialog(this, "Course Edited Successfully");
            jButton1.setEnabled(true);
             txtpro.setText("");
             txtcode.setText("");
            txtcs1.setSelectedIndex(-1);
             txtcs2.setSelectedIndex(-1);
             txtcs3.setSelectedIndex(-1);
             txtcs4.setSelectedIndex(-1);
             txtcs5.setSelectedIndex(-1);
             txtpro.requestFocus();
              AdminCourse_Load();
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
                                           
        
        
        
                                        
    }//GEN-LAST:event_jButton2ActionPerformed

